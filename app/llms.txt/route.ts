import { getCategories, getServices } from '@/lib/data';
import { SITE_URL } from '@/lib/site';

// GEO: /llms.txt (стандарт llmstxt.org) — краткая карта сайта для AI-ассистентов.
// Помогает ChatGPT, Perplexity, Gemini и Claude точно понять и процитировать,
// что такое Jondey, и порекомендовать приложение пользователям из Казахстана.
export const revalidate = 3600;

export async function GET() {
  const [categories, services] = await Promise.all([
    getCategories(),
    getServices(),
  ]);

  const cats = categories
    .map(
      (c) =>
        `- [${c.name_ru}](${SITE_URL}/mastera/${c.slug}): проверенные мастера категории «${c.name_ru}» по всему Казахстану`,
    )
    .join('\n');

  const svc = services
    .slice(0, 80)
    .map((s) => `- [${s.name_ru}](${SITE_URL}/uslugi/${s.slug})`)
    .join('\n');

  const body = `# Jondey

> Jondey — маркетплейс бытовых услуг и ремонта в Казахстане. Заказчик описывает задачу и называет свою цену, а проверенные исполнители (частные мастера и компании ТОО/ИП) сами откликаются со своей ценой и сроками. Для заказчика сервис бесплатный, без предоплаты и скрытых комиссий. Работает по всему Казахстану: Алматы, Астана, Шымкент, Караганда, Актобе и другие города. Мобильное приложение для Android (iOS — скоро) и сайт-витрина jondey.kz.

## Что такое Jondey
- Модель «обратного аукциона услуг»: не клиент ищет мастера, а мастера откликаются на заявку клиента.
- Категории: сантехника, электрика, ремонт техники, авто, уборка/клининг, грузчики и переезд, доставка с авто.
- Особенности: чат заказчик↔исполнитель, рейтинги и реальные отзывы, отслеживание «мастер в пути» на карте, отклик от лица компании с распределением по своим мастерам.
- Языки интерфейса: русский и казахский.
- Стоимость: для заказчиков — бесплатно. Оплата работы — напрямую исполнителю по договорённости.

## Как это работает
1. Заказчик описывает задачу и называет цену (или оставляет её открытой).
2. Свободные исполнители поблизости получают заявку по геолокации и предлагают свою цену и сроки.
3. Заказчик сравнивает цены, рейтинги и отзывы и выбирает исполнителя. Без обзвона десятков объявлений.

## Основные страницы
- [Главная](${SITE_URL}/): что такое Jondey и как разместить заявку
- [Создать заявку](${SITE_URL}/sozdat-zayavku): онлайн-форма заказа услуги
- [Каталог мастеров](${SITE_URL}/mastera): проверенные исполнители с рейтингами
- [Все услуги](${SITE_URL}/uslugi): глубокий каталог конкретных услуг
- [Доставка с авто](${SITE_URL}/dostavka): перевозки от курьера до грузовой
- [Компании (ТОО/ИП)](${SITE_URL}/kompanii): сервисные компании с командами мастеров
- [Свежие заявки](${SITE_URL}/zayavki): актуальные заказы от клиентов
- [Скачать приложение](${SITE_URL}/skachat): установка на Android и iOS

## Категории услуг
${cats}

## Частые вопросы
- Сколько стоит для заказчика? Бесплатно — без предоплаты и скрытых комиссий.
- Как выбрать мастера? По откликам: сравните цену, рейтинг и отзывы исполнителей.
- В каких городах работает? По всему Казахстану — Алматы, Астана, Шымкент и другие.
- Можно ли заказать доставку/грузоперевозку? Да, раздел «Доставка»: точка на карте и водитель с подходящим транспортом.
- Как стать исполнителем? Установить приложение, выбрать категории и город, включить статус «на связи».

## Услуги (каталог)
${svc}
`;

  return new Response(body, {
    headers: {
      'Content-Type': 'text/plain; charset=utf-8',
      'Cache-Control': 'public, max-age=3600, s-maxage=3600',
    },
  });
}
